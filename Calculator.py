class Calculator:
    def __init__(self, number1, number2):
        self.x=number1
        self.y=number2
    def add(self):
        return self.x+self.y
    def sub(self):
        return self.x-self.y
    def mul(self):
        return self.x*self.y
    def div(self):
        return self.x//self.y
